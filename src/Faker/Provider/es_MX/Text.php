      velozmente su vuelo desde el pantano. Nubes de humo azul
      se esparcieron por el lugar. Los perros de caza aparecieron
      chapaleando entre el agua buscando a las presas. Aquello
      aterrorizó al pobre patito feo, que ya se disponía a ocultar
