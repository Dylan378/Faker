      a ninguno de los otros. Y, sin embargo, me atrevo a asegurar
      que no es ningún crío de pavos.

      Al otro día, el sol resplandecía en las verdes hojas gigantescas.
      La mamá pata se acercó al foso con toda su familia y, plaf!,
      saltó al agua.
      – Cuac, cuac!–, llamaba. Y uno tras otro los patitos
      fueron saltando tras ella. Se sumergían pero enseguida
      reaparecían flotando magníficamente. Movían sus patas sin
      el menor esfuerzo. Hasta el enorme patito feo y gris nadaba
      con los otros.
      –No es un pavo, –pensó la pata. –Nada con elegancia
      y si uno lo mira bien, se da cuenta de que es muy guapo.
      – Cuac, cuac! Vengan conmigo y voy a presentarlos al corral
      entero. Pero no se separen mucho de mí, para que no los pisoteen.
      Y anden con los ojos muy abiertos, por si viene el gato.
      – A ver!–, exclamó la pata. –Caminen más rápido y
