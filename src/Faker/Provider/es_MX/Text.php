      El viento soplaba ferozmente. En un momento, el patito
      descubrió un agujero en la tabla de la puerta y le resultó
      fácil filtrarse por esa estrecha abertura.

      En la cabaña vivía una anciana con su gato y su gallina.
      El gato, a quien la anciana llamaba “Hijito”, sabía arquear
      el lomo y ronronear; la gallina era una gran ponedora y
      la anciana la quería como a su propia hija.
      Cuando llegó la mañana, el gato y la gallina no tardaron en
      descubrir al extraño patito. El gato lo saludó ronroneando y
      la gallina con su cacareo.
      –Pero, qué pasa?–, preguntó la vieja, mirando a su
      alrededor. No andaba muy bien de la vista, así que se creyó
      que el patito feo era una pata. – Qué suerte!– dijo. Ahora
      tendremos huevos de pata.

