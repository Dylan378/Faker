      Los días del verano eran hermosos. Qué
      agradable resultaba pasear por el campo y ver
      el trigo amarillo, la avena verde y las parvas
      de heno apiladas en la llanura!
      Se alzaba a lo lejos una vieja mansión rodeada de un
      profundo foso; desde sus paredes hasta el borde del agua
      crecían unas plantas de hojas gigantescas, lo suficientemente
      grandes como para que un niño pudiese esconderse debajo
      de ellas. En ese lugar agreste, una pata había hecho su nido.
      Ya era tiempo de que naciesen sus patitos, pero se demoraban
      mucho y la pata empezaba a impacientarse porque casi nadie
      venía a visitarla.
      Al fin los huevos se abrieron uno tras otro.
      –“ Pi, pi, pi!”–, decían los patitos al asomar sus cabecitas
      a través del cascarón.

      – Cuac, cuac!– dijo la mamá pata, y los patitos se
      apresuraron a salir tan rápido como pudieron y se dedicaron
      a observar las verdes hojas que rodeaban el nido. La mamá
      los dejó hacer, pues el verde es muy bueno para los ojos.
      –Bueno, espero que ya estén todos, pensó la pata.
      Ah, pero si todavía falta el huevo más grande!–, exclamó
      al mirar el nido. – Cuánto tardará aún?
      Y fue a sentarse de nuevo en su sitio.
      – Vaya, vaya! Cómo anda eso?–, preguntó una pata
      vieja que venía de visita.
      –Ya no queda más que este huevo– dijo la pata echada.
