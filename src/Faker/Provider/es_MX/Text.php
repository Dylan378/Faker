      a aquellas espléndidas aves que una vez había visto levantar
      el vuelo.

      – Volaré hasta ellas!– se dijo. –Me darán picotazos por
      haberme atrevido a aproximarme, feo como soy. Pero,
      qué importa! Mejor es que ellas me maten a sufrir los pellizcos
      de los patos, los picotazos de las gallinas, los golpes de la
      muchacha que cuida las aves y la nieve del invierno.
      Y así, se dirigió hacia los hermosos cisnes. En cuanto lo
      vieron, se le acercaron con las plumas encrespadas.
      – Sí, mátenme, mátenme!–, gritó el patito, inclinando
      la cabeza hacia el agua en espera de los picotazos. Pero,
      qué es lo que vio en el limpio arroyo? Era un reflejo
      de sí mismo, pero no era ya el reflejo de un ave torpe y gris
      sino el reflejo de un cisne!

      Poco importa nacer en el corral de los patos, siempre que
      uno salga de un huevo de cisne. Se sentía realmente feliz.
      Los tres cisnes nadaban y nadaban a su alrededor y
      lo acariciaban con sus picos.
      En el jardín habían entrado unos niños que lanzaban al agua
      pedazos de pan y semillas. El más pequeño exclamó:
      – Ahí va un nuevo cisne! Qué joven y esbelto es!
      Y los otros niños corearon con gritos de alegría:
      – Sí, hay un cisne nuevo! Es el más hermoso!
      Y los cisnes viejos seguían a su lado. Era muy, pero muy feliz.
      Las lilas inclinaron sus ramas ante él, y los rayos del sol eran
      tibios y brillantes. Rizó entonces sus alas, alzó el esbelto
      cuello y se alegró desde lo hondo de su corazón:
      –Jamás soñé que podría haber tanta felicidad, allá
      en los tiempos en que era sólo un patito feo.
