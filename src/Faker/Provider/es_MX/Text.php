      resplandeciente, y tenían cuellos largos y esbeltos. De
      pronto, lanzaron un fantástico grito, extendieron sus
      magníficas alas y remontaron el vuelo, alejándose de aquel
      frío hacia las tierras cálidas.

      El patito feo se sintió lleno de una rara inquietud. Jamás
      podría olvidar a aquellos hermosos pájaros! No tenía idea de
      cuál podría ser el nombre de las aves y, sin embargo, sabía
      que eran muy importantes para él.

      Cuán frío se presentó aquel invierno! El patito se veía
      forzado a nadar incesantemente para impedir que el agua
      se congelase en torno suyo. Pero cayó una noche una helada
      tan fuerte que el patito, debilitado por el esfuerzo, se quedó
      quieto y comenzó a congelarse rápidamente.

      A la mañana siguiente, muy temprano, lo encontró
