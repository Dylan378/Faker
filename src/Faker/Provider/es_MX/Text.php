      desanimado. Pero de pronto recordó el aire fresco y el sol,
      y sintió una gran nostalgia de irse a nadar.
      –Creo que me voy a recorrer el ancho mundo–, dijo
      el patito.
      –Sí, vete–, dijo la gallina.
