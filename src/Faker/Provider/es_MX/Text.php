      –No es hermoso, señora–, dijo la mamá de
      los patitos. –Pero tiene buen carácter y nada tan bien
      como los otros. Su aspecto va a mejorar cuando crezca.
      Y con el pico le acarició el cuello y le alisó las plumas.
      –Es que sus otros patitos son tan encantadores–, dijo
      la vieja pata.

      Con esos comentarios, todos los patitos se sintieron a sus
      anchas en el corral. Pero el pobre patito que tan feo
      les parecía a todos, empezó a recibir picotazos, empujones
      y burlas, de los patos y de las gallinas.
      – Qué feo es!–, decían.
      Así pasó el primer día. En los días siguientes, las cosas
      empeoraron. Hasta sus hermanos y hermanas maltrataban
      al patito y le decían:
      – Ojalá te agarre el gato, grandulón!

      Los patos lo pellizcaban, las gallinas lo picoteaban y,
      un día, la muchacha que traía la comida para las aves le
      dio un puntapié.
