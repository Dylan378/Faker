      la cerca agitando sus alas. Los pajaritos que estaban en
      los árboles se asustaron y echaron a volar por los aires.
      – Es porque soy tan feo!–, pensó el patito, cerrando
      los ojos. Y siguió corriendo hasta que, por fin, llegó a
      los grandes pantanos donde viven los patos salvajes, y allí
      se pasó toda la noche abrumado de cansancio y tristeza.

