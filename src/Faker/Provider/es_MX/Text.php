      entre los juncos y tomar un poquito de agua del pantano.
      Unos días más tarde aparecieron por allí dos gansos salvajes.
      –Mira, muchacho–, comenzaron diciéndole, –eres tan
      feo que nos caes simpático. Quieres venir con nosotros?
      Pero en ese instante, por encima de ellos, se escuchó:
      Bang, bang, bang!

