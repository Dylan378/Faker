      madera, lo recogió y lo llevó a casa, donde su mujer se
      encargó de revivirlo al calor del hogar.

      Los niños querían jugar con él, pero el patito feo tenía terror
      de sus travesuras y, con el miedo, fue a meterse revoloteando
      en un cuenco de leche, que se derramó por todo el piso. Gritó
      la mujer y él, más asustado todavía, se metió de un vuelo en
